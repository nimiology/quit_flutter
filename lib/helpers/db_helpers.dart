import 'package:sqflite/sqflite.dart' as sql;
import 'package:path/path.dart' as path;
import 'package:sqflite/sqlite_api.dart';

class DBHelper {
  static Future<Database> database() async {
    final dbPath = await sql.getDatabasesPath();
    return sql.openDatabase(path.join(dbPath, 'places.db'),
        onCreate: (db, version) {
      return db.execute(
          "CREATE TABLE quit_period(id TEXT PRIMARY KEY, title TEXT NOT NULL, created_date INTEGER NOT NULL);"
          "CREATE TABLE doing(id TEXT PRIMARY KEY, quit_period_id INTEGER NOT NULL, title TEXT, created_date INTEGER NOT NULL,"
          "FOREIGN KEY (A_id) REFERENCES A (id) ON DELETE CASCADE ON UPDATE NO ACTION,)");
    }, version: 1);
  }

  static Future<void> insert(String table, Map<String, Object> data) async {
    final db = await DBHelper.database();
    db.insert(
      table,
      data,
      conflictAlgorithm: ConflictAlgorithm.replace,
    );
  }

  static Future<List<Map<String, dynamic>>> getData(String table) async {
    final db = await DBHelper.database();
    return db.query(table);
  }
}
